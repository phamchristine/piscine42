int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb < 0)
		return (0);
	if (nb <= 1)
		return (nb);
	while (i <= nb / i)
	{
		if (i == nb / i && nb % i == 0)
			return (i);
		i++;
	}
	return (0);
}

#include <stdio.h>

int main ()
{
	printf("%d", ft_sqrt(10000));
}
