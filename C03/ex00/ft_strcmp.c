#include <stdio.h>
#include <string.h>

int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	return (s1[i] - s2[i]);
}

int		main(void)
{
	char a[] = "iiii";
	char b[] = "";
	printf("%d", ft_strcmp(a, b));
	printf("%d", strcmp(a, b));
	return (0);
}
